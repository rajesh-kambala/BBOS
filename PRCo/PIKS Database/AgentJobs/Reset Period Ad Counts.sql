		@description=N'Resets the period impression and click counts.  A period is whatever BBSI defines it to be, i.e. however often this job executes.', 
		@category_name=N'BBSI', 
		@owner_login_name=N'BBSAgentJobs', 
		@notify_email_operator_name=N'SQL Support Notifications', @job_id = @jobId OUTPUT
